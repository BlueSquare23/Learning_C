#include<stdio.h>
#include <stdlib.h>

int main(){

	printf("%f\n", pow(4, 3));	// pow() is the power of function for exponents
	printf("%f\n", sqrt(36));	// sqrt() is the square root funtion
	printf("%f\n", ceil(25.01));	// ceil() rounds up to the nearest int (no matter remainder)
	printf("%f\n", floor(25.99));	// floor() rounds down to nearest int (no matter remainder)

	return 0;
}
